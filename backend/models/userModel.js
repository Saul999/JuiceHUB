const mongoose = require("mongoose");

const { Schema } = mongoose;

export const UserSchema = new Schema(
  {
    username: {
      type: String,
      required: true,
      unique: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
    },
    password: {
      type: String,
      required: true,
    },
    // Other properties if needed
  },
  { timestamps: true, strict: true }
);
